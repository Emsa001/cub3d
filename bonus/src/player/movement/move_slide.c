#include "cub3d.h"

void	sliding(t_player *p, int *speed)
{
	if (p->slide)
	{
		*speed += 300;
		if (p->z > 0.3)
			p->z -= 0.05;
	}
	else if (p->z < 0.5)
		p->z += 0.01;
}
